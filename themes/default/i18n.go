func defaultThemeT(lang, key string) string {
	key = strings.TrimSpace(key)
	if key == "" {
		return ""
	}
	if strings.EqualFold(lang, "zh-CN") || strings.EqualFold(lang, "zh_CN") || strings.HasPrefix(strings.ToLower(lang), "zh") {
		if value := strings.TrimSpace(defaultThemeZhCN[key]); value != "" {
			return value
		}
	}
	return key
}
